let currentGenre = null;

function showMovies(genre) {
  const list = document.getElementById('movie-list');

  // Если уже выбран этот жанр — скрываем
  if (currentGenre === genre) {
    list.innerHTML = '';
    currentGenre = null;
    return;
  }

  // Показываем фильмы нового жанра
  const movieItems = movies[genre].map(title => `<li>${title}</li>`).join('');
  list.innerHTML = `<h3>Фильмы жанра: ${genre.toUpperCase()}</h3><ul>${movieItems}</ul>`;
  currentGenre = genre;
}


